#include <stdio.h>


int		ft_strlen(char *str)
{
	int n;

	n = 0;
	while (str[n] != '\0'){
		n++;
	}
	return (n);
}

int main(void){
	int a = ft_strlen("12345");
	printf("%d\n", a);
}
