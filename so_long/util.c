#include "solong.h"

void	mapcpy(int	**dst, int **src, int x, int y)
{
	int i;
	int j;

	i = 0;
	while (i < y)
	{
		j = 0;
		while (j < x)
			dst[i][j] = src[i][j];
	}
}
